#!/usr/bin/env node
/**
 * Relink the database from Cloudinary delivery URLs to ImageKit.
 *
 * Because restore-imagekit.mjs preserved each asset's public_id as its ImageKit
 * file path, the rewrite is deterministic and a single regex handles every URL:
 *
 *   https://res.cloudinary.com/<cloud>/image/upload/[transforms/][v<n>/]<publicId>.<ext>
 *     -> <URL_ENDPOINT>/<publicId>.<ext>
 *
 * Generates relink-imagekit.sql (regexp_replace per URL-bearing column, scoped to
 * rows that still reference Cloudinary). Review it, then apply via the Supabase
 * SQL editor or MCP apply_migration.
 *
 * If any restored asset did NOT land at its deterministic URL (recorded in
 * restore-result.json as matches=false), per-asset exact-replace overrides are
 * appended so those rows are fixed too.
 *
 *   node cloudinary-backup/relink-db-imagekit.mjs
 */
import { readFile, writeFile } from 'node:fs/promises'
import { existsSync, readFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const DIR = dirname(fileURLToPath(import.meta.url))
const ROOT = join(DIR, '..')
const RESULT = join(DIR, 'restore-result.json')
const OUT = join(DIR, 'relink-imagekit.sql')

function loadEnv() {
  const envPath = join(ROOT, '.env.local')
  if (!existsSync(envPath)) return
  for (const line of readFileSync(envPath, 'utf8').split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const idx = trimmed.indexOf('=')
    if (idx < 0) continue
    const key = trimmed.slice(0, idx)
    if (!process.env[key]) process.env[key] = trimmed.slice(idx + 1)
  }
}
loadEnv()

const URL_ENDPOINT = (process.env.NEXT_PUBLIC_IMAGEKIT_URL_ENDPOINT || '').replace(/\/$/, '')
if (!URL_ENDPOINT) {
  console.error('Missing NEXT_PUBLIC_IMAGEKIT_URL_ENDPOINT.')
  process.exit(1)
}

// Every (table, column) pair that can contain an image URL. Kept in sync with
// relink-db.mjs.
const TEXT_COLUMNS = [
  ['menu_items', 'image_url'],
  ['bundles', 'image_url'],
  ['categories', 'icon'],
  ['tenants', 'logo_url'],
  ['tenants', 'flash_screen_image_url'],
  ['tenants', 'promotion_image_url'],
  ['payment_methods', 'qr_code_url'],
  ['platform_payment_methods', 'qr_code_url'],
  ['orders', 'payment_method_qr_code_url'],
  ['checkout_leads', 'payment_proof_url'],
]
const JSONB_COLUMNS = [
  ['menu_items', 'addons'],
  ['menu_items', 'variation_types'],
  ['menu_items', 'variations'],
  ['categories', 'default_addons'],
  ['tenants', 'promotion_banners'],
]

// Only rewrite URLs for clouds we actually restored into ImageKit. URLs from the
// lost cloud (dns9deszp) are intentionally left untouched so they stay
// identifiable as "needs manual re-upload" instead of becoming ImageKit 404s.
const restoredClouds = (() => {
  if (!existsSync(RESULT)) {
    console.error('restore-result.json not found — run restore-imagekit.mjs first.')
    process.exit(1)
  }
  const result = JSON.parse(readFileSync(RESULT, 'utf8'))
  const set = new Set()
  for (const [oldUrl, r] of Object.entries(result)) {
    if (r.status !== 'ok') continue
    try {
      set.add(new URL(oldUrl).pathname.split('/').filter(Boolean)[0])
    } catch {
      /* ignore unparseable */
    }
  }
  return [...set]
})()

if (restoredClouds.length === 0) {
  console.error('No successfully-restored clouds found in restore-result.json.')
  process.exit(1)
}
console.log('Restored clouds (will be relinked):', restoredClouds.join(', '))

// Postgres POSIX regex: strip host + (restored cloud) + /image/upload/ + optional
// transform segments + optional version, leaving <publicId>.<ext>. Transform
// segments are "<key>_<val>,..." groups; the version is "v<digits>".
const CLOUD_ALT = `(${restoredClouds.map((c) => c.replace(/[.\\]/g, '\\$&')).join('|')})`
const CL = `https://res\\.cloudinary\\.com/${CLOUD_ALT}/image/upload/`
const OPT_TRANSFORM = '([a-z]+_[^/]+/)*' // e.g. "w_400,c_fill/"
const OPT_VERSION = '(v[0-9]+/)?'
const PATTERN = `${CL}${OPT_TRANSFORM}${OPT_VERSION}`
const REPLACEMENT = `${URL_ENDPOINT}/`

const lines = [
  '-- Auto-generated by relink-db-imagekit.mjs. Review before running.',
  '-- Rewrites Cloudinary delivery URLs to ImageKit (deterministic path-preserving).',
  '-- Idempotent: only rows still referencing res.cloudinary.com are touched.',
  'BEGIN;',
  '',
]

// WHERE guard: only rows referencing a restored cloud (leaves dns9deszp alone).
const whereFor = (expr) =>
  restoredClouds.map((cl) => `${expr} LIKE '%/${cl}/%'`).join(' OR ')

for (const [t, c] of TEXT_COLUMNS) {
  lines.push(
    `UPDATE ${t} SET ${c} = regexp_replace(${c}, '${PATTERN}', '${REPLACEMENT}', 'g') ` +
      `WHERE ${whereFor(c)};`,
  )
}
lines.push('')
for (const [t, c] of JSONB_COLUMNS) {
  lines.push(
    `UPDATE ${t} SET ${c} = regexp_replace(${c}::text, '${PATTERN}', '${REPLACEMENT}', 'g')::jsonb ` +
      `WHERE ${whereFor(`${c}::text`)};`,
  )
}

// Per-asset overrides for any upload that didn't land at the deterministic URL.
if (existsSync(RESULT)) {
  const result = JSON.parse(readFileSync(RESULT, 'utf8'))
  const overrides = Object.entries(result).filter(
    ([, r]) => r.status === 'ok' && r.matches === false && r.url && r.expected,
  )
  if (overrides.length > 0) {
    lines.push('', `-- ${overrides.length} per-asset override(s) (non-deterministic upload URLs)`)
    for (const [, r] of overrides) {
      // After the regex pass the row holds r.expected; correct it to the actual r.url.
      const from = r.expected.replace(/'/g, "''")
      const to = r.url.replace(/'/g, "''")
      for (const [t, c] of TEXT_COLUMNS) {
        lines.push(`UPDATE ${t} SET ${c} = replace(${c}, '${from}', '${to}') WHERE ${c} LIKE '%${from}%';`)
      }
      for (const [t, c] of JSONB_COLUMNS) {
        lines.push(
          `UPDATE ${t} SET ${c} = replace(${c}::text, '${from}', '${to}')::jsonb WHERE ${c}::text LIKE '%${from}%';`,
        )
      }
    }
  }
}

lines.push('', 'COMMIT;', '')

await writeFile(OUT, lines.join('\n'))
console.log(`Wrote ${OUT}`)
console.log('Review it, then apply via the Supabase SQL editor / MCP apply_migration.')
